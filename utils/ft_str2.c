#include "minishell.h"

int	doc_strcmp(char *s1, char *s2)
{
	if (!*s2 && !s1)
		return (0);
	if (!s2 || !s1)
		return (1);
	while (*s1 == *s2 && *s1)
	{
		s1++;
		s2++;
	}
	if (*s1 == '\n' && !*s2)
		return (0);
	return (*s1 - *s2);
}
