#include "minishell.h"

void	_env_(void)
{
	char		**tmp;

	tmp = environ;
	while (*tmp)
	{
		if (ft_strchr(*tmp, '=') && **tmp != '?')
			printf("%s\n", *tmp);
		tmp++;
	}
	_setenv("?", ft_itoa(0));
}
